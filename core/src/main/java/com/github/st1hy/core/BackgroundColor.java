package com.github.st1hy.core;

public class BackgroundColor {
    final float r , g, b , a;

    public BackgroundColor(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }

    public BackgroundColor() {
        r = 1;
        g = 1;
        b = 1;
        a = 1;
    }
}
